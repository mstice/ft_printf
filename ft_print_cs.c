#include "printf.h"

int	ft_print_c(char c)
{
	write (1, &c, 1);
	return (1);
}

int	ft_print_s(char *s)
{
	unsigned int	i;

	i = 0;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
